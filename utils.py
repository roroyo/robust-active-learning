class Averager(object):
    """Compute average for torch.Tensor, used for loss average."""

    def __init__(self):
        self.reset()

    def add(self, v):
        count = v.data.numel()
        v = v.data.sum()
        self.n_count += count
        self.sum += v

    def reset(self):
        self.n_count = 0
        self.sum = 0

    def val(self):
        res = 0
        if self.n_count != 0:
            res = self.sum / float(self.n_count)
        return res



class EceLogger():
    def __init__(self) -> None:
        self.reset()

    def add(self, ece, acc, ned, n=6):
        if len(self.ece_acc) >= n:
            if ece < self.ece_acc[-1][0] or (ece == self.ece_acc[-1][0] and acc >= self.ece_acc[-1][1]): 
                self.ece_acc[-1] = (ece, acc, ned)
        else:
            self.ece_acc.append((ece, acc, ned))
        self.ece_acc = sorted(self.ece_acc, key=lambda x:x[0])

    def reset(self):
        self.ece_acc = []

    def show(self):
        dashed_line = '-' * 80
        ece_str = f'  ECE  '
        acc_str = f'  ACC  '
        ned_str = f'  NED  '
        for ece, acc, ned in self.ece_acc:
            ece_str += f'|  {ece:.5f}  '
            acc_str += f'|  {acc:.5f} '
            ned_str += f'|  {ned:.5f}  '
        print(f'{dashed_line}\n{ece_str}\n{acc_str}\n{ned_str}')
        return f'{dashed_line}\n{ece_str}\n{acc_str}\n{ned_str}'

